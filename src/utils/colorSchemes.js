// Helper function to get color scheme by name
export const getColorScheme = (schemeName) => {
  return colorSchemes[schemeName] || colorSchemes.blue; // fallback to blue
};

// Helper function to get all available color scheme names
export const getAvailableColorSchemes = () => {
  return Object.keys(colorSchemes);
};

// Helper function to generate card hover classes
export const getCardHoverClasses = (colorScheme) => {
  const scheme = getColorScheme(colorScheme);
  return `${scheme.hover.border} ${scheme.hover.shadow}`;
};

// Helper function to generate technology badge classes
export const getTechBadgeClasses = (colorScheme) => {
  const scheme = getColorScheme(colorScheme);
  return `${scheme.background.primary} ${scheme.text.secondary} ${scheme.hover.bg} ${scheme.hover.shadow}`;
};

// Helper function to generate button classes
export const getButtonClasses = (colorScheme) => {
  const scheme = getColorScheme(colorScheme);
  return `${scheme.background.primary} ${scheme.text.secondary} ${
    scheme.hover.text
  } ${scheme.hover.bg} ${scheme.border.primary} ${scheme.hover.border.replace(
    "hover:border-",
    "hover:border-"
  )} ${scheme.hover.shadow}`;
};
